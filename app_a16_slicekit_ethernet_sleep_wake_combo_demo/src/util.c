/*---------------------------------------------------------------------------
 copy_server_config_to_char_array
 ---------------------------------------------------------------------------*/
void copy_server_config_to_char_array(server_config_t *sconfig, char *data)
{
  memcpy(data, (char *)(sconfig), sizeof(server_config_t));
}

/*---------------------------------------------------------------------------
 copy_char_array_to_server_config
 ---------------------------------------------------------------------------*/
void copy_char_array_to_server_config(server_config_t *sconfig, char *data)
{
  memcpy((char *)(sconfig), data, sizeof(server_config_t));
}

